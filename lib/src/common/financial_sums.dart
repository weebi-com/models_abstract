abstract class FinancialSums {
  final num sell;
  final num sellCovered;
  final num sellDeferred;
  final num spend;
  final num spendCovered;
  final num spendDeferred;
  final num wage;
  const FinancialSums(
    this.sell,
    this.sellCovered,
    this.sellDeferred,
    this.spend,
    this.spendCovered,
    this.spendDeferred,
    this.wage,
  );
}
