abstract class FinancialSums {
  final int sell;
  final int sellCovered;
  final int sellDeferred;
  final int spend;
  final int spendCovered;
  final int spendDeferred;
  final int wage;
  const FinancialSums(
    this.sell,
    this.sellCovered,
    this.sellDeferred,
    this.spend,
    this.spendCovered,
    this.spendDeferred,
    this.wage,
  );
}
